package cibertec.edu.pe.beans;

public class Ciudad {
	private String idCiu;
	private String desCiu;
	private String idPais;
	
	public String getIdCiu() {
		return idCiu;
	}
	public void setIdCiu(String idCiu) {
		this.idCiu = idCiu;
	}
	public String getDesCiu() {
		return desCiu;
	}
	public void setDesCiu(String desCiu) {
		this.desCiu = desCiu;
	}
	public String getIdPais() {
		return idPais;
	}
	public void setIdPais(String idPais) {
		this.idPais = idPais;
	}

}
